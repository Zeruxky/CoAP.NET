namespace WorldDirect.CoAP.V1.Options
{
    /// <summary>
    /// Represents the available byte orders.
    /// </summary>
    public enum ByteOrder
    {
        /// <summary>
        /// Defines that the byte order is little endian.
        /// </summary>
        LittleEndian,

        /// <summary>
        /// Defines that the byte order is big endian (network-byte-order)
        /// </summary>
        BigEndian,
    }
}
